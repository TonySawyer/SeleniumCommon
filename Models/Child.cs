namespace UITest.RegressionCommon.Models
{
    /// <summary> The child. </summary>
    public class Child
    {
        /// <summary> Gets or sets the current school index. </summary>
        public int CurrentSchoolIndex { get; set; }

        /// <summary> Gets or sets the dob. </summary>
        public string Dob { get; set; }

        /// <summary> Gets or sets the ethnicity index. </summary>
        public int EthnicityIndex { get; set; }

        /// <summary> Gets or sets the first language index. </summary>
        public int FirstLanguageIndex { get; set; }

        /// <summary> Gets or sets the forename. </summary>
        public string Forename { get; set; }

        /// <summary> Gets or sets the gender. </summary>
        public string Gender { get; set; }

        /// <summary> Gets or sets the house number. </summary>
        public string HouseNumber { get; set; }

        /// <summary> Gets or sets the middle name. </summary>
        public string MiddleName { get; set; }

        /// <summary> Gets or sets a value indicating whether parental responsibility. </summary>
        public bool ParentalResponsibility { get; set; }

        /// <summary> Gets or sets the postcode. </summary>
        public string Postcode { get; set; }

        /// <summary> Gets or sets the relationship to child index. </summary>
        public int RelationshipToChildIndex { get; set; }

        /// <summary> Gets or sets the surname. </summary>
        public string Surname { get; set; }

        /// <summary> Gets or sets a value indicating whether use parents address. </summary>
        public bool UseParentsAddress { get; set; }
    }
}